import React, { useEffect, useMemo, useState } from "react";
import PropTypes from "prop-types";
import toast from "react-hot-toast";
import {
  Button, Chip, Divider, Input, Select, SelectItem, Spacer, Switch, Textarea, Tooltip,
} from "@heroui/react";
import { LuInfo, LuRefreshCw, LuSave } from "react-icons/lu";
import { useDispatch, useSelector } from "react-redux";

import { runRequest, updateDataset } from "../../slices/dataset";
import { FIELD_ROLE_OPTIONS, getDefaultAggregation, getFieldTypeColor, getMetricAggregationOptions, normalizeEditableFieldsMetadata, sortDatasetFieldsMetadata } from "../../modules/datasetFieldMetadata";
import { selectTeam } from "../../slices/team";

function DatasetFields({ dataset }) {
  const [fieldsMetadata, setFieldsMetadata] = useState([]);
  const [isSaving, setIsSaving] = useState(false);
  const [isRefreshing, setIsRefreshing] = useState(false);

  const dispatch = useDispatch();
  const team = useSelector(selectTeam);

  useEffect(() => {
    setFieldsMetadata(sortDatasetFieldsMetadata(dataset?.fieldsMetadata || []));
  }, [dataset?.fieldsMetadata]);

  const hasUnsavedChanges = useMemo(() => {
    return JSON.stringify(normalizeEditableFieldsMetadata(fieldsMetadata))
      !== JSON.stringify(normalizeEditableFieldsMetadata(dataset?.fieldsMetadata || []));
  }, [fieldsMetadata, dataset?.fieldsMetadata]);

  const _updateField = (fieldId, patch) => {
    setFieldsMetadata((prev) => sortDatasetFieldsMetadata(prev.map((field) => {
      if (field.id !== fieldId) {
        return field;
      }

      const nextField = {
        ...field,
        ...patch,
      };

      if (patch.role && patch.role !== "metric") {
        nextField.aggregation = "none";
      } else if (
        patch.role === "metric"
        && (
          !nextField.aggregation
          || nextField.aggregation === "none"
          || !getMetricAggregationOptions(nextField.type).some((option) => option.value === nextField.aggregation)
        )
      ) {
        nextField.aggregation = getDefaultAggregation("metric", nextField.type);
      }

      nextField.autoGenerated = false;

      return nextField;
    })));
  };

  const _onSaveFields = async () => {
    if (!dataset?.id || !team?.id) {
      return;
    }

    setIsSaving(true);
    try {
      await dispatch(updateDataset({
        team_id: team.id,
        dataset_id: dataset.id,
        data: {
          fieldsMetadata: normalizeEditableFieldsMetadata(fieldsMetadata),
        },
      })).unwrap();

      toast.success("Field metadata updated");
    } catch (error) {
      toast.error("Could not save field metadata");
    } finally {
      setIsSaving(false);
    }
  };

  const _onRefreshFields = async () => {
    if (!dataset?.id || !team?.id) {
      return;
    }

    setIsRefreshing(true);
    try {
      await dispatch(runRequest({
        team_id: team.id,
        dataset_id: dataset.id,
        getCache: false,
      })).unwrap();

      toast.success("Fields refreshed from preview data");
    } catch (error) {
      toast.error("Could not refresh fields");
    } finally {
      setIsRefreshing(false);
    }
  };

  if (!dataset?.fieldsMetadata?.length) {
    return (
      <div className="bg-content1 rounded-lg border-1 border-divider p-6">
        <div className="text-lg font-semibold">Fields</div>
        <Spacer y={2} />
        <div className="text-sm text-default-500">
          No field metadata has been generated yet. Run a fresh preview to infer fields from the dataset response.
        </div>
        <Spacer y={4} />
        <Button
          color="primary"
          onPress={_onRefreshFields}
          isLoading={isRefreshing}
          isDisabled={!dataset?.DataRequests?.length}
          startContent={!isRefreshing ? <LuRefreshCw /> : null}
        >
          Refresh fields
        </Button>
      </div>
    );
  }

  return (
    <div className="bg-content1 rounded-lg border-1 border-divider p-4">
      <div className="flex flex-row justify-between items-center flex-wrap gap-3">
        <div>
          <div className="text-lg font-semibold">Fields</div>
          <div className="text-sm text-default-500">
            Review inferred fields, adjust their role, and save overrides for reuse across charts.
          </div>
        </div>
        <div className="flex flex-row gap-2 flex-wrap">
          <Button
            variant="flat"
            onPress={_onRefreshFields}
            isLoading={isRefreshing}
            startContent={!isRefreshing ? <LuRefreshCw /> : null}
          >
            Refresh fields
          </Button>
          <Button
            color="primary"
            onPress={_onSaveFields}
            isLoading={isSaving}
            isDisabled={!hasUnsavedChanges}
            startContent={!isSaving ? <LuSave /> : null}
          >
            Save overrides
          </Button>
        </div>
      </div>

      <Spacer y={4} />
      <div className="flex flex-row gap-2 flex-wrap">
        <Chip variant="flat" color="primary">{`${fieldsMetadata.filter((field) => field.role === "metric" && field.missing !== true).length} metrics`}</Chip>
        <Chip variant="flat" color="secondary">{`${fieldsMetadata.filter((field) => field.role === "date" && field.missing !== true).length} date fields`}</Chip>
        <Chip variant="flat" color="success">{`${fieldsMetadata.filter((field) => field.role === "dimension" && field.missing !== true).length} dimensions`}</Chip>
      </div>

      <Spacer y={4} />
      <div className="flex flex-col gap-4">
        {fieldsMetadata.map((field) => (
          <div key={field.id} className="rounded-lg border-1 border-divider bg-background p-4">
            <div className="flex flex-row justify-between gap-3 flex-wrap items-start">
              <div className="flex flex-col gap-1">
                <div className="flex flex-row items-center gap-2 flex-wrap">
                  <div className="font-semibold">{field.label}</div>
                  <Chip size="sm" variant="flat" color={getFieldTypeColor(field.type)}>
                    {field.type || "unknown"}
                  </Chip>
                  <Chip size="sm" variant="bordered">
                    {field.role}
                  </Chip>
                  {field.missing === true && (
                    <Chip size="sm" color="warning" variant="bordered">
                      Missing from latest preview
                    </Chip>
                  )}
                </div>
                <div className="text-xs text-default-500 break-all">{field.legacyPath || field.id}</div>
              </div>
              <div className="flex flex-row gap-4 flex-wrap">
                <Switch
                  isSelected={field.enabled !== false}
                  onValueChange={(enabled) => _updateField(field.id, { enabled })}
                  size="sm"
                >
                  Enabled
                </Switch>
                <Switch
                  isSelected={field.excludeFromCharts !== true}
                  onValueChange={(useInCharts) => _updateField(field.id, { excludeFromCharts: !useInCharts })}
                  size="sm"
                >
                  Use in charts
                </Switch>
              </div>
            </div>

            <Spacer y={3} />
            <div className="grid grid-cols-12 gap-3">
              <div className="col-span-12 md:col-span-4">
                <Input
                  label="Label"
                  labelPlacement="outside"
                  variant="bordered"
                  value={field.label || ""}
                  onChange={(event) => _updateField(field.id, { label: event.target.value })}
                />
              </div>
              <div className="col-span-12 md:col-span-3">
                <Select
                  label="Role"
                  labelPlacement="outside"
                  variant="bordered"
                  selectedKeys={[field.role]}
                  onSelectionChange={(keys) => _updateField(field.id, { role: keys.currentKey })}
                >
                  {FIELD_ROLE_OPTIONS.map((option) => (
                    <SelectItem key={option.value}>
                      {option.label}
                    </SelectItem>
                  ))}
                </Select>
              </div>
              <div className="col-span-12 md:col-span-3">
                <Select
                  label="Aggregation"
                  labelPlacement="outside"
                  variant="bordered"
                  selectedKeys={[field.role === "metric" ? field.aggregation : "none"]}
                  isDisabled={field.role !== "metric"}
                  onSelectionChange={(keys) => _updateField(field.id, { aggregation: keys.currentKey })}
                >
                  {getMetricAggregationOptions(field.type).map((option) => (
                    <SelectItem key={option.value}>
                      {option.text}
                    </SelectItem>
                  ))}
                </Select>
              </div>
              <div className="col-span-12 md:col-span-2">
                <div className="text-xs uppercase tracking-wide text-default-400 mb-2">Sample</div>
                <Tooltip
                  content={field.previewSample?.length > 0 ? field.previewSample.join(", ") : "No sample values"}
                >
                  <Chip variant="flat" className="max-w-full truncate">
                    {field.previewSample?.[0] ?? "No sample"}
                  </Chip>
                </Tooltip>
              </div>
              <div className="col-span-12">
                <Textarea
                  label="Description"
                  labelPlacement="outside"
                  variant="bordered"
                  minRows={2}
                  value={field.description || ""}
                  onChange={(event) => _updateField(field.id, { description: event.target.value })}
                />
              </div>
            </div>

            <Spacer y={2} />
            <Divider />
            <Spacer y={2} />
            <div className="flex flex-row items-center gap-2 text-xs text-default-500">
              <LuInfo />
              <span>
                Refresh preserves saved overrides for fields that still exist in the latest preview. "Use in charts" controls whether the field appears in the V2 visualize builder.
              </span>
            </div>
          </div>
        ))}
      </div>
    </div>
  );
}

DatasetFields.propTypes = {
  dataset: PropTypes.object,
};

export default DatasetFields;
